
public class VariableAssign {

	public static void main(String[] args) {
		
		int value1 = 10;
		double value2 = 56.9;
		boolean value3 = false;
		char value4 = 'S';
		String value5 = "Prisca";
		boolean value6 = true;
		int value7 = 347890;
		long value8 = 2147483650L;
		
		System.out.println("Here are the values: "+value1+", "+value2+", "+value3+", "+value4+", "+value5+", "+value6+", "+value7+", "+value8);
		
		
		int numInch = 47;
		int numFeet = numInch/12;
		int remInch = numInch%12;
		
		System.out.println(numInch+" inches is equal to "+numFeet+" feet and "+remInch+" inch/es.");

	}

}
